<h1>Conditions générales de vente</h1>
<h4>1. Droit de révocation et réclamations au sujet de la marchandise
</h4>  
<p>La commande, quel que soit le moyen de paiement choisi, est ferme et définitive. L’acheteur ne dispose d’aucun droit de rétractation ou de révocation de sa commande et ne peut donc restituer la marchandise commandée. Pour le surplus, toute réclamation concernant la marchandise doit être faite par écrit auprès de Elie Gazzar SA dans les 8 jours dès la livraison. 
</p>
<h4>2. Goût de bouchon</h4>
<p>
Elie Gazzar S.A. calcule les prix de vente de ses vins au plus juste. Ceux-ci n'incluent pas de marge pour les bouteilles présentant un éventuel goût de bouchon. Aussi, un cas isolé de goût de bouchon ne peut donner droit à aucune prétention de la part du client.

<h4>3. Interdiction de vente à des mineurs</h4>
<p>
Par son acceptation des conditions générales, le client certifie qu’il est âgé d’au moins 18 ans. En effet, est prohibée expressément par la loi la vente de spiritueux, d’apéritifs et d’alcopops aux personnes de moins de 18 ans, ainsi que la vente de vins, cidres et bières aux moins de 16 ans.
</p>
<h4>4. Cas spécial des vins vendus en primeurs</h4>
<p>
La commande ne sera honorée que si elle est payée d’avance dans les trente jours.

En raison de la modification du paiement de la TVA à l’administration fédérale des Contributions, celle-ci se fait dès la fin de 2009 lors de l’encaissement chaque trimestre.

Si, sans la faute d’Elie Gazzar SA, les vins vendus en primeurs commandées à ses fournisseurs ne lui sont pas livrées, le client n’aura droit qu’au remboursement du prix qu’il a payé, à l’exclusion de tous autres dommages intérêts.
</p>
<h4>5. Moment du paiement</h4>
<p>
Elie Gazzar SA exige systématiquement de ses nouveaux clients le paiement par avance, c’est-à-dire avant la livraison. Dans les autres cas, sauf convention contraire entre les parties, le délai de paiement est de trente jours net dès la commande.
</p>
<h4>6. Participation aux frais de livraison</h4>
<p>
Fr. 20.- jusqu'à 12 bouteilles de 75 cl.
Fr. 30.- pour 24 bouteilles de 75 cl.

Dès 36 bouteilles de 75 cl., la livraison est gratuite dans toute la Suisse.
</p>
